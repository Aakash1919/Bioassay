<span class="textbold_orange">General Recommendations</span><br /><br />
  <p><table style="width:700px"><col style="width:40%"><col style="width:60%">
     <tr><td>Store reagents properly</td><td>Read the data sheet carefully and store reagents in their proper location. Some kits require different components to be stored at different temperatures.<br /><br /></td></tr>
     <tr><td>Equilibrate reagents to assay temperature</td><td>Use an incubator or water bath to equilibrate all reagents (except enzymes) to assay temperature. Enzymes should be thawed and kept on ice or at 4&deg;C during assays.<br /><br /></td></tr>
     <tr><td>Run a test standard curve</td><td>Make sure that there is nothing faulty with the reagents and to ensure that you are following the directions carefully. Your standard curve should be linear unless otherwise noted in data sheet and generate similar results to the example graph.<br /><br /></td></tr>
     <tr><td>Preliminary sample serial dilution</td><td>If this is your first time running a particular type of sample, use micro-centrifuge tubes to make a 2x serial dilution of your sample. Run the assay with the original and diluted samples to determine if dilution is necessary and which dilution factor is optimal. Use the chosen dilution factor in all subsequent experiments.<br /><br /></td></tr>
     <tr><td>Pipette carefully</td><td>Consistency between wells is very important; carefully pipette down the side of the well and ensure that all wells have the same volume and all reagents flow down to the bottom. Take special care to avoid bubbles, which can disrupt absorbance readings.<br /><br /></td></tr>
  </table></p><br /><br />

