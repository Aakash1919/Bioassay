<div align="center"><span class="textbold_orange">PTP1b Activity Assay Control Tests<br /> K<sub>m</sub> for Full Length and Truncated PTP1b and IC50 Determination for Sodium Vanadate (Na<sub>3</sub>VO<sub>4</sub>)</span><br /><br /></div>

<span class="textbold_orange">Summary</span><br />
Before determining the IC50’s for ten potential PTP1b inhibitors, we performed some initial experiments to establish appropriate 
assay conditions. We assessed conditions for a full length human PTP1b and a truncated human PTP1b (Glu2-Asn321). First we titrated 
the PTP1b’s using 5 mM pNPP as the substrate.  From these titrations we decided to use 50 ng/reaction for the full length PTP1b and 
40 ng/rxn for the truncated PTP1b. We next titrated DMSO to determine the highest concentration that would be tolerated by each 
PTP1b. The Km of pNPP for each PTP1b was then measured in the presence of the highest tolerable DMSO concentration. Finally, the 
IC50 for a known tyrosine phosphatase inhibitor, Na<sub>3</sub>VO<sub>4</sub> was measured for each PTP1b. For the full length PTP1b, we determined 
the maximum DMSO concentration to be 15 v% and the Km to be 0.7 ± 0.04 mM.  For the truncated PTP1b, we determined the maximum 
DMSO concentration to be 7.5 v% and the Km to be 1.3 ± 0.1 mM. Using 0.7 mM pNPP, we observed an IC50 = 19.3 ± 1.1 mM for 
Na<sub>3</sub>VO<sub>4</sub> for the full length PTP1b.  Using 1 mM pNPP, we observed an IC50 = 54.5 ± 1.1 mM for Na<sub>3</sub>VO<sub>4</sub> for the truncated PTP1b. 
