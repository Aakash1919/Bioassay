<span class="textbold_orange">Results</span><br />
<i>PTP1b Titrations</i><br />
In order to determine an appropriate amount of PTP1b to use for the IC50 determinations, we titrated each PTP1b. The full length 
PTP1b was titrated from 75 ng/rxn and the truncated PTP1b was titrated from 200 ng/rxn.  Both titrations were performed using 
5 mM pNPP as the substrate.  The full length PTP1b titration was performed in a 384 well plate to maximize the absorbance signal. 
(Due to high variation between replicates, we decided to switch to 96 well plates for the IC50 analysis and all of the 
truncated PTP1b analyses.)<br /><br />

