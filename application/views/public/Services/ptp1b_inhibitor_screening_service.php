        <img src="/images/PTP1B.Inhibitor.Screening.Service2.jpg" width="300" height="247" alt="PTP1B.Inhibitor.Screening.Service figure2"></div><br />

From these results we opted to use 50 ng/rxn for the full length PTP1b and 40 ng/rxn for the truncated PTP1b. At these concentrations 
the reaction would remain linear for at least 1 hr and the ∆OD should be significant (>0.2) even at the pNPP Km.<br /><br />

<i>DMSO Titration </i><br />
In order to determine tolerance for DMSO of each of the PTP1b’s, we ran DMSO titrations for each PTP1b (from 0–27 v% for the full 
length PTP1b and from 0-15 v% for the truncated PTP1b). <br /><br />

   <div><img src="/images/PTP1B.Inhibitor.Screening.Service3.jpg" width="319" height="247" alt="PTP1B.Inhibitor.Screening.Service figure3">
        <img src="/images/PTP1B.Inhibitor.Screening.Service4.jpg" width="300" height="247" alt="PTP1B.Inhibitor.Screening.Service figure4"></div><br />

For these titrations we used 50 ng per reaction for the full length PTP1b and 40 ng truncated PTP1b.  The reactions were initiated 
by adding 5 mM pNPP and were allowed to proceed for 60 min.  We then computed the ∆OD (OD60min – OD0min), which is proportional to 
the rate of reaction, for each DMSO concentration.  The ∆OD’s were then plotted versus DMSO concentration.  From these results we 
discovered that DMSO actually enhanced the activity of each PTP1b up to a certain concentration.  For the full length PTP1b, we 
decided to move forward with 15 v% DMSO and for the truncated PTP1b, we decided on 7.5 v% DMSO.<br /><br />

<i>Km of pNPP</i><br />
In order to determine the Km of pNPP for the PTP1b’s, we titrated pNPP from 0–20 mM with either 50 ng full length PTP1b or 40 ng 
truncated PTP1b and computed ∆OD (OD60min – OD0min), which is proportional to the rate of reaction, for each pNPP concentration. 
The ∆OD’s were then plotted versus pNPP and the Km was computed using Prism 4 (GraphPad Software Inc.). The full length PTP1b Km 
determination was performed in the presence of 15 v% DMSO and truncated PTP1b Km determination was performed in the presence of 
7.5 v% DMSO. We found the Km = 0.7 ± 0.04 mM for the full length PTP1b and the Km = 1.3 ± 0.1 mM for the truncated PTP1b.<br /><br />

