<span class="textbold_orange">Sodium Vanadate (Na<sub>3</sub>VO<sub>4</sub>) IC50 Determination</span><br />
The IC50 determination required two steps: 1) 30 min pre-incubation of 25 µL PTP1b (50 ng full length PTP1b or 40 ng truncated 
PTP1b) with 15 µL Na<sub>3</sub>VO<sub>4</sub> (in 100% DMSO for full length PTP1b or 50 v% DMSO for truncated PTP1b), and 2) phosphatase reaction with 
60 µL pNPP (0.7 mM final [pNPP] for full length PTP1b and 1 mM final [pNPP] for truncated PTP1b) for 60 min.  We titrated Na<sub>3</sub>VO<sub>4</sub> 
from 0-1000 mM and computed the ∆OD for each Na<sub>3</sub>VO<sub>4</sub> concentration.  The ∆OD’s were then plotted versus the Log [Na<sub>3</sub>VO<sub>4</sub>] and the 
IC50 was computed using Prism 4 (GraphPad Software Inc.).  We observed an IC50 = 19.3 ± 1.1 mM for the full length PTP1b and an 
IC50 = 54.5 ± 1.1 mM for the truncated PTP1b.<br /><br />


