<span class="textbold_orange">Polymer Quantification</span><br /><br />

<span class="textbold_orange">Summary</span><br />
BioAssay Systems has developed a kinetic fluorometric assay to quantify D-lactate containing polymers, such as PLA and 
PLGA in tissue samples. The assay principle is based on the hydrolysis of D-lactate containing polymers into lactate 
monomers followed by D-lactate determination using a highly sensitive and specific fluorescence method. Only L-lactate 
is naturally prevalent in animal tissue (D-lactate is typically present at only nM levels in mammals which is below the 
detection limit of the current assay); therefore, any D-lactate measured in a sample is solely derived from the polymer 
