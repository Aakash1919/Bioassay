<span class="textbold_orange">Conclusions</span><br />
Bioassay Systems has successfully performed this service to measure the degradation of polymer coatings on implanted devices 
for 3 different medical device companies.  Results from these projects were critical for two CE Mark Approvals and the FDA 
approval of two medical device Investigational Device Exemption (IDE) applications. </div><br />

This service includes the final preparation of samples for the assay, the hydrolysis of polymer in samples down to monomers and 
enzymatic measurement of the resultant D-lactate monomers.  The polymer weight in the sample is computed from the measured 
D-lactate concentration in the hydrolysis reaction.  The client will be issued a report summarizing the results of the 
analysis along with an Excel spreadsheet containing all of the collected data, data analysis and results.<br /><br />
