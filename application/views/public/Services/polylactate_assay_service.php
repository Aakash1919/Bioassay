<span class="textbold_orange">Key Features of the Assay</span><br />
<ul/>
<li>Useful to assess remaining polymer coatings on implanted medical devices.</li>
<li>Two step process: 1) Hydrolysis of polymer present to monomers and 2) Measurement of D-lactate.</li>
<li>Accuracy and sensitivity of the assay are not affected by polymer MW, which will decrease over time during implantation.</li>
<li>The method uses D-lactate as a biomarker and since D-lactate is not found endogenously above nM concentrations, the assay is highly specific for PLA.</li>
<li>There is no requirement to remove the tissue from sample, the PLA on both the device and in the tissue are measured together.</li>
<li>Depending on the nature of the sample and polymer composition, sensitivities as low as 1 microgram are possible.</li><br />
</ul>
