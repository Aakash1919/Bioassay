						<span class="textbold_orange">Luminex Assay Principle</span>
						
						<div id=luminex-diagram">
							<div style="width: 100%;">
								<div id="step1">
									<img id = "step1-img" alt="Multiplex Luminex Assay Principle Step 1: The sample is added to color-coded beads, pre-coated with analyte-specific capture antibodies." src="/images/luminex-step1.png" title="Multiplex Luminex Assay Principle Step 1">
									<br />
									<br />
									The sample is added to a mixture of color-coded beads, pre-coated with analyte-specific capture antibodies. The antibodies bind to the analytes of interest.
								</div>
								<div id="step2">
									<img id= "step2-img" alt="Biotinylated detection antibodies are added and form an antibody-antigen sandwich." src="/images/luminex-step2.png" title="Multiplex Luminex Assay Principle Step 2">
									<br />
									<br />
									Biotinylated detection antibodies specific to the analytes of interest are added and form an antibody-antigen sandwich. Phycoerythrin (PE)-conjugated streptavidin is added. It binds to the biotinylated detection antibodies.							
								</div>
							</div>
							
							<div class="clear">
							</div>
							
							<br />

							<div id="step3">
								<div style="text-align: center;">
									<img id="step3-img" alt="Beads are read on a dual-laser flow-based detection instrument." title="Multiplex Luminex Assay Principle Step 3" src="/images/luminex-step3.png">
								</div>
								<div>
									<div id="step3a">
									Beads are read on a dual-laser flow-based detection instrument, such as the Luminex 200<sup>™</sup> or FlexMap<sup>®</sup> analyzer. One laser classifies the bead and determines the analyte that is being detected. The second laser determines the magnitude of the PE-derived signal, which is in direct proportion to the amount of analyte bound.
									</div>
									<div id="step3b">
									In addition to flow-based analyzers, magnetic beads can be read using the Luminex MAGPIX<sup>®</sup> Analyzer. A magnet in the MAGPIX analyzer captures and holds the magnetic beads in a monolayer, while two spectrally distinct light-emitting diodes (LEDs) illuminate the beads. One LED identifies the analyte that is being detected and, the second LED determines the magnitude of the PE-derived signal. Each well is imaged with a CCD camera.
									</div>
