<span class="textbold_orange">Summary</span><br />
The solubility of a drug compound plays a significant role in absorption and is an important factor to
consider during development. Quantitative measurement of compound solubility is an ideal early test to
perform when screening for potential drug candidates.
<br />
<br />
BioAssay Systems (BAS) offers two methods for quantifying the solubility of drug compounds: filter
plate solubility testing and shake flask solubility testing. This information page details the Filter Plate
Method. 
 
