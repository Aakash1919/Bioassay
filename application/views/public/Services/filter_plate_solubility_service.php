<span class="textbold_orange">Method</span><br />
In MultiScreen filter plate, the compound is mixed with a solvent of customer's choice. The plate is
gently shaken for 1.5 hours at ambient temperature (or desired temperature). The solution is vacuum
filtered. Absorbance spectrum is run on the filtrate together with the test compound standards.
Concentration of the filtrate is calculated using the slope of the standards. 
