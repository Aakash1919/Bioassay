<i>ABH IC50 Determination</i><br />
The IC50 determination required three steps: 1) 30 min pre-incubation of 25 ng arginase I with ABH, 2) arginase 
reaction with 3 mM L-arginine for 25 min and 3) urea concentration measurement. We titrated ABH from 0-10 &micro;M and 
computed the rate of reaction, V, for each ABH concentration.  The rates of reaction were then plotted versus 
the Log [ABH] and the IC50 was computed using Prism 4 (GraphPad Software Inc). We observed an IC50 = 0.14 ± 0.01 &micro;M.<br /><br />

