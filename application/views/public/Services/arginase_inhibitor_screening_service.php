<div align="center"><span class="textbold_orange">Arginase Activity Assay Control Tests: K<sub>m</sub> for Arginase I and IC50 Determination for ABH</span><br /><br /></div>

<span class="textbold_orange">Summary</span><br />
In order to test the suitability of BioAssay Systems Arginase Assay for screening for arginase inhibitors, we performed some 
initial experiments to determine the Km of L-arginine for a purified human arginase and the IC50 for a known arginase 
inhibitor, ABH.  We measured a K<sub>m</sub> of 3.4 ± 0.2 mM.  Using 3 mM L-arginine, we observed an IC50 = 0.14 ± 0.1 &micro;M for ABH.  
We also verified that 2 v% DMSO will not affect the assay. 
