<i>Effect of DMSO</i><br />
In order to determine if DMSO would interfere with this arginase assay, we set up four reactions: 25 ng arginase I
± 2 v% DMSO and 0 ng arginase ± 2 v% DMSO. Following a 20 min reaction and addition of the Urea Reagent, we measured 
the OD<sub>520</sub> and OD<sub>430</sub> and computed the ΔOD (ODarginase – ODno arginase) for the 2 v% DMSO and 0 v% 
DMSO reactions at each wavelength. The following table displays the results:<br /><br />

