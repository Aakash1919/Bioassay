<span class="textbold_orange">Results</span><br />
<i>K<sub>m</sub> of L-Arginine</i><br />
In order to determine the K<sub>m</sub> of L-arginine for Arginase I, we titrated L-arginine from 0–20 mM with 25 ng Arginase I 
and computed the rates of reaction, V (&micro;mol/min/mg), for each L-arginine concentration.  The rates of reaction were 
then plotted versus L-arginine and the Km was computed using Prism 4 (GraphPad Software Inc). We found the 
K<sub>m</sub> = 3.4 ± 0.2 mM.<br /><br />

