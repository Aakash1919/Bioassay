Clearly 2 v% DMSO has no adverse effects on the assay and will not be an issue for the compound screen.<br /><br />

<span class="textbold_orange">Conclusions</span><br />
The results for the K<sub>m</sub> of L-arginine for arginase I and IC50 of ABH observed in this study agree well with numbers 
found in literature (within 3 fold).  This study also verified that 2 v% DMSO will not interfere with the assay. 
Thus, this arginase assay will be suitable for screening for arginase inhibitors.<br /><br /><br />

