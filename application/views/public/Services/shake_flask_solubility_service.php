<span class="textbold_orange">Method</span><br />
In 2 mL tubes, an excess of the test compound is weighed out and mixed with a solvent of customer's
choice. The tubes are shaken for 18 hours (or desired duration) at ambient temperature (or desired
temperature). The solution is vacuum filtered to remove precipitates. Absorbance spectrum is run on the
filtrate together with the test compound standards. Concentration of the filtrate is calculated using the
slope of the standards.
