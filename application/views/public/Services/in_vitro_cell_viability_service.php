<span class="textbold_orange">Summary</span><br />
Toxicity is a crucial factor to test when evaluating test compounds as potential drug candidates. In most
cases, test compounds should not be detrimental to the health of the cells they are treating. In order to
evaluate the toxicity of test compounds, BioAssay Systems offers three different methods of measuring
in vitro cell viability.
<br />
<br />
After treatment with test compounds, cell viability can be evaluated by the following methods:
<br />
1. The levels of intracellular ATP as an indicator of healthy cells;
<br />
2. Levels of Lactate Dehydrogenase (LDH) excreted into the cell media from damaged cells as an
indicator of toxicity;
<br />
3. Cellular reduction capacity using either a fluorescent or colorimetric dye as a measure of viability.
 
