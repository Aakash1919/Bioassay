<span class="textbold_orange">Method</span><br />
The cell line of interest is plated at the specified cell density and the respective positive controls,
negative controls, and treatments are added to the media. After incubation with the treatments for the
specified time, the respective reagent for the desired test method is added to the wells. The wells are
then read via luminescence, fluorescence, or absorbance either immediately or after further incubation
depending on the test method.
<br />
Compounds can be evaluated for dose response, where the cytotoxicity is evaluated at various
concentrations for a set treatment duration, or for time response, where cytotoxicity of a specific
concentration is measured at time points throughout the course of treatment.
