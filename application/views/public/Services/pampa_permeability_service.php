<span class="textbold_orange">Summary</span><br />
Due to the membrane barriers most drugs must pass through to reach their target, the permeability of a drug compound is an important factor to consider during development. Quantitative measurement of compound permeability is an ideal early test to perform when screening for potential drug candidates.
<br />
<br />
One of the Permeability tests BioAssay Systems offers is the Parallel Artificial Membrane Permeability Assay (PAMPA). This test measures passive diffusion of compounds and is ideal for early stage drug development because it is fast and low cost relative to other cell-based permeability assays. 
 
